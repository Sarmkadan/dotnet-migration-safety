using System.Collections.Immutable;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Diagnostics;

namespace MigrationSafety.Analyzers
{
	/// <summary>
	/// Flags destructive or lock-heavy operations inside EF Core migrations before they reach
	/// production. Every rule inspects <c>migrationBuilder.*</c> invocations in the syntax tree
	/// and reports on the method-name token so the squiggle lands on the operation itself.
	/// </summary>
	[DiagnosticAnalyzer(LanguageNames.CSharp)]
	public sealed class MigrationSafetyAnalyzer : DiagnosticAnalyzer
	{
		public override ImmutableArray<DiagnosticDescriptor> SupportedDiagnostics { get; } =
			ImmutableArray.Create(
				Descriptors.DropColumn,
				Descriptors.NonConcurrentIndex,
				Descriptors.TableRewrite,
				Descriptors.DropTable,
				Descriptors.AddNotNullColumn,
                Descriptors.RenameColumn);

		public override void Initialize(AnalysisContext context)
		{
			context.EnableConcurrentExecution();
			// Migration files are generated code and are the entire point of this
			// analyzer, so opt in to analyzing and reporting on them. With the default
			// (None), any file carrying an <auto-generated> header - e.g. migration
			// Designer files - would silently be skipped.
			context.ConfigureGeneratedCodeAnalysis(
				GeneratedCodeAnalysisFlags.Analyze | GeneratedCodeAnalysisFlags.ReportDiagnostics);
			context.RegisterSyntaxNodeAction(AnalyzeInvocation, SyntaxKind.InvocationExpression);
		}

		private static void AnalyzeInvocation(SyntaxNodeAnalysisContext context)
		{
			var invocation = (InvocationExpressionSyntax)context.Node;
			if (!(invocation.Expression is MemberAccessExpressionSyntax memberAccess))
			{
				return;
			}

			if (!(context.SemanticModel.GetSymbolInfo(invocation).Symbol is IMethodSymbol method))
			{
				return;
			}

			if (!MigrationBuilderCalls.IsMigrationBuilderMethod(method))
			{
				return;
			}

			if (SuppressionComment.IsReviewed(invocation))
			{
				return;
			}

			var reportLocation = memberAccess.Name.GetLocation();

			switch (method.Name)
			{
			case "DropColumn":
				Report(context, invocation, method, Descriptors.DropColumn, reportLocation, "name", "table");
				break;

			case "DropTable":
				ReportSingle(context, invocation, method, Descriptors.DropTable, reportLocation, "name");
				break;

			case "CreateIndex":
				Report(context, invocation, method, Descriptors.NonConcurrentIndex, reportLocation, "name", "table");
				break;

			case "AlterColumn":
				Report(context, invocation, method, Descriptors.TableRewrite, reportLocation, "name", "table");
				break;

			case "AddColumn":
				AnalyzeAddColumn(context, invocation, method, reportLocation);
				break;

            case "RenameColumn":
                Report(context, invocation, method, Descriptors.RenameColumn, reportLocation, "name", "newName");
                break;

            case "RenameTable":
                Report(context, invocation, method, Descriptors.RenameColumn, reportLocation, "name", "newName");
                break;
			}
		}

		private static void AnalyzeAddColumn(
			SyntaxNodeAnalysisContext context,
			InvocationExpressionSyntax invocation,
			IMethodSymbol method,
			Location reportLocation)
		{
			// Check if nullable: false is explicitly set
			var nullableArg = MigrationBuilderCalls.GetNamedArgument(invocation, method, "nullable");
			if (nullableArg != null)
			{
				// Check if the nullable argument is the literal false
				var expression = nullableArg.Expression;
				if (expression is LiteralExpressionSyntax literal && literal.Token.Value is false)
				{
					// Check if defaultValue or defaultValueSql is present
					var hasDefaultValue = MigrationBuilderCalls.GetNamedArgument(invocation, method, "defaultValue") != null;
					var hasDefaultValueSql = MigrationBuilderCalls.GetNamedArgument(invocation, method, "defaultValueSql") != null;

					if (!hasDefaultValue && !hasDefaultValueSql)
					{
						context.ReportDiagnostic(Diagnostic.Create(
							Descriptors.AddNotNullColumn,
							reportLocation,
							method.Name));
					}
				}
			}
		}

		private static void Report(
			SyntaxNodeAnalysisContext context,
			InvocationExpressionSyntax invocation,
			IMethodSymbol method,
			DiagnosticDescriptor descriptor,
			Location location,
			string firstArg,
			string secondArg)
		{
			var first = MigrationBuilderCalls.GetStringArgument(invocation, method, firstArg) ?? "?";
			var second = MigrationBuilderCalls.GetStringArgument(invocation, method, secondArg) ?? "?";
			context.ReportDiagnostic(Diagnostic.Create(descriptor, location, first, second));
		}

		private static void ReportSingle(
			SyntaxNodeAnalysisContext context,
			InvocationExpressionSyntax invocation,
			IMethodSymbol method,
			DiagnosticDescriptor descriptor,
			Location location,
			string firstArg)
		{
			var first = MigrationBuilderCalls.GetStringArgument(invocation, method, firstArg) ?? "?";
			context.ReportDiagnostic(Diagnostic.Create(descriptor, location, first));
		}
	}
}
